def A_Ex8(fileName):
    fin=open(fileName,'r',encoding='UTF-8')
    contrig=0
    res=0
    for rig in fin:
        contrig+=1
        rig=rig.strip().split()
        for let in rig:
            if let.isupper():
                res+=1
    return contrig            
            
